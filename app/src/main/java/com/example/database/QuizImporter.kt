    suspend fun importFromAssets(context: Context) {
        withContext(Dispatchers.IO) {
            val db = EktefaaDatabase.getDatabase(context)
            val currentQuizzes = db.quizDao().getAllQuizzes()
            if (currentQuizzes.isNotEmpty()) {
                return@withContext // Quizzes already imported
            }

            for ((index, lesson) in LESSONS.withIndex()) {
                val title = lesson.first
                var bodyText = lesson.second

                // Try to load from assets first (graceful fall-back if not present on disk)
                try {
                    val assetFileName = "quizzes/dars${index + 1}.txt"
                    context.assets.open(assetFileName).bufferedReader().use { reader ->
                        val text = reader.readText()
                        if (text.isNotBlank()) {
                            bodyText = text
                        }
                    }
                } catch (e: Exception) {
                    // fall-back to internal LESSONS text
                }

                val quiz = QuizEntity(
                    id = (index + 1).toLong(),
                    title = title,
                    sourceText = bodyText,
                    type = when (index % 4) {
                        0 -> "mcq"
                        1 -> "truefalse"
                        2 -> "fillblank"
                        else -> "crossword"
                    }
                )

                db.quizDao().insertQuiz(quiz)

                // Generate exactly 15 questions per quiz (5 MCQ, 5 TF, 5 FillBlank)
                val mcqs = QuizGenerator.generateMCQ(bodyText, quiz.id, 5)
                val tfs = QuizGenerator.generateTrueFalse(bodyText, quiz.id, 5)
                val fbs = QuizGenerator.generateFillBlank(bodyText, quiz.id, 5)

                db.questionDao().insertQuestions(mcqs + tfs + fbs)
            }

            // Insert placeholder family scores if empty
            val currentLeaderboard = db.familyScoreDao().getLeaderboardFlow().first()
            if (currentLeaderboard.isEmpty()) {
                db.familyScoreDao().insertScore(FamilyScoreEntity(memberName = "الأب (المشرف)", quizId = 1, score = 15, totalQuestions = 15))
                db.familyScoreDao().insertScore(FamilyScoreEntity(memberName = "الأم", quizId = 1, score = 14, totalQuestions = 15))
                db.familyScoreDao().insertScore(FamilyScoreEntity(memberName = "الابن الأكبر", quizId = 1, score = 12, totalQuestions = 15))
            }

            // Insert initial Wisdom if empty
            val currentWisdom = db.dailyWisdomDao().getLatestWisdom()
            if (currentWisdom == null) {
                db.dailyWisdomDao().insertWisdom(
                    DailyWisdomEntity(
                        text = "قِيمَةُ كُلِّ امْرِئٍ مَا يُحْسِنُهُ. التمسك بالوعي والعمل الصالح هما درعا التمكين.",
                        source = "نهج البلاغة - الإمام علي عليه السلام",
                        date = "2026-06-12",
                        type = "wisdom"
                    )
                )
            }
        }
    }
